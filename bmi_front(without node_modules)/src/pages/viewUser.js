import axios from "axios";
import React, { useEffect,useState } from "react";
import { Link, useParams } from "react-router-dom";

export default function viewUser() {

  const [user,setUser] =useState({
  firstName:"",
  lastName:"",
  dateOfBirth:"",
  gender:"",
  weight:"",
  height:""
})
// bmi calculate formula
const bmi = (user.weight/(user.height*user.height)).toFixed(2); // örnek bir BMI değeri

function getBMICategory(bmi) {
  if (bmi < 16) {
    return "Severe Thinness";
  } else if (bmi >= 16 && bmi < 17) {
    return "Moderate Thinness";
  } else if (bmi >= 17 && bmi < 18.5) {
    return "Mild Thinness";
  } else if (bmi >= 18.5 && bmi < 25) {
    return "Normal";
  } else if (bmi >= 25 && bmi < 30) {
    return "Overweight";
  } else if (bmi >= 30 && bmi < 35) {
    return "Obese Class I";
  } else if (bmi >= 35 && bmi < 40) {
    return "Obese Class II";
  } else {
    return "Obese Class III";
  }
}

let category = getBMICategory(bmi);

  const { id } = useParams();

  useEffect(() => {
    loadUser();
  }, []);

  const loadUser = async () => {
    const result = await axios.get(`http://localhost:8080/persons/${id}`);
    setUser(result.data);
  };

  return (
    <div className="container">
      <div className="row">          
        <div className="col-md-6 offset-md-3 border rounded p-4 mt-2 shadow">
          <h2 className="text-center m-4">User Details</h2> 
          <div className="card">
            <div className="card-header">
              Details of user id : {user.id}
              <ul className="list-group list-group-flush">
                <li className="list-group-item">
                  <b>First Name : </b>
                  {user.firstName}
                </li>
                <li className="list-group-item">
                  <b>Last Name : </b>
                  {user.lastName}
                </li>
                <li className="list-group-item">
                  <b>Date of Birth : </b>
                  {user.dateOfBirth}
                </li>
                <li className="list-group-item">
                  <b>Gender : </b>
                  {user.gender}
                </li>
                <li className="list-group-item">
                  <b>Weight : </b>
                  {user.weight}
                </li>
                <li className="list-group-item">
                  <b>Height : </b>
                  {user.height}
                </li>              
                <li className="list-group-item">
                  <b>Entry Date : </b>
                  {user.entryDate}
                </li>
                <li className="list-group-item">
                  <b>BMI : </b>
                  {(user.weight/(user.height*user.height)).toFixed(2)} 
                </li>
                <li className="list-group-item">
                  <b>Category : </b>
                  {category}
                </li>                 
              </ul>
            </div>
          </div>        
          <Link className="btn btn-primary my-2" to={"/"}>
            Back to Home
          </Link>
        </div>
      </div>
    </div>
  );
}