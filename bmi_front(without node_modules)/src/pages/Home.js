import React, { useEffect, useState } from 'react'
import axios from 'axios' // For access datas wia html.
import { Link, useParams } from 'react-router-dom';

export default function Home() {
    const [users,setUsers] = useState([])
    const {id} = useParams();

    useEffect(() => {
        loadUsers();
    });

    const loadUsers= async ()=> {
        const result = await axios.get("http://localhost:8080/persons");
        setUsers(result.data);
        console.log(result.data);
    }

    const deleteUser= async (id)=> {
      await axios.delete(`http://localhost:8080/persons/${id}`)
      loadUsers()

    }

  return (
    <div className='container'>
        <div className='py-4'>
        <table className="table border shadow">
  <thead>
    <tr>
      <th scope="col">ID</th>
      <th scope="col">Name Surname</th>
      <th scope="col">Date Of Birth</th>
      <th scope="col">BMI</th>
      <th scope="col">Actions</th>
    </tr>
  </thead>
  <tbody>

    {
        users.map((user,index)=>(
        <tr>
        <td>{user.id}</td>
        <td>{user.firstName + " "+user.lastName}</td>
        <td>{user.dateOfBirth}</td>        
        <td>{(user.weight/(user.height*user.height)).toFixed(2)}</td>
        <td>
        <Link className="btn btn-primary mx-2" to={`/viewuser/${user.id}`} > View </Link>
            <button className='btn btn-danger mx-2' onClick={()=>deleteUser(user.id)} > Delete </button>
        </td>
        </tr>
        ))
    }
    
  </tbody>
</table>
        </div>
    </div>
  )
}
